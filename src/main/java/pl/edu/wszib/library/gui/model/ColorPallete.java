package pl.edu.wszib.library.gui.model;


import java.awt.*;

public class ColorPallete {

    private Color lightGray= new java.awt.Color(237, 242, 244);
    private Color gray = new java.awt.Color(141, 153, 174);
    private Color darkGray = new java.awt.Color(43, 45, 66);
    private Color lightRed = new java.awt.Color(239, 35, 60);
    private Color red = new java.awt.Color(217, 4, 41);

    public Color getLightGray() {
        return lightGray;
    }

    public Color getGray() {
        return gray;
    }

    public Color getDarkGray() {
        return darkGray;
    }

    public Color getLightRed() {
        return lightRed;
    }

    public Color getRed() {
        return red;
    }

    public ColorPallete() {
    }


}
